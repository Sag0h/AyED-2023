package practica1b.ejercicio5;

public class MaxMinProm {
	private int min;
	private int max;
	private float promedio;
	
	public MaxMinProm(int max, int min, float promedio) {
		super();
		this.min = min;
		this.max = max;
		this.promedio = promedio;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public float getPromedio() {
		return promedio;
	}

	public void setPromedio(float promedio) {
		this.promedio = promedio;
	}
	
	
	
	
}
